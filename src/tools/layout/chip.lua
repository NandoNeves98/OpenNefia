local function gen_crop(list, source, folder)
   local l = {}
   for _, v in ipairs(list) do
      local height = 48
      if v.tall then
         height = 96
      end

      local width = 48

      local id = v.id
      local x = (id % 33) * 48
      local y = math.floor(id / 33) * 48

      if v.count_x then
         for i=1,v.count_x do
            local output = string.format("graphic/%s/%s_%d.png", folder, v.name, i)

            l[#l+1] = {
               type = "crop",
               source = source,
               x = x + ((i-1) * width),
               y = y,
               width = width,
               height = height,
               output = output
            }
         end
      else
         local output = string.format("graphic/%s/%s.png", folder, v.name)

         l[#l+1] = {
            type = "crop",
            source = source,
            x = x,
            y = y,
            width = width,
            height = height,
            output = output
         }
      end
   end

   return l
end

